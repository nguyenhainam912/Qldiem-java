    public void loaddata() {
        
        try {
            DefaultTableModel model = (DefaultTableModel) tbLopbienche.getModel();
            model.setRowCount(0);
            ArrayList<LopBienChe> list = LopBienCheDAO.list();
            for (LopBienChe item : list) {
                model.addRow(new Object[]{item.getId(), item.getTen_lop(), item.getId_giangvien()});
            }
        } catch (Exception e ) {
            
        }
    }
